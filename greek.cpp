#include <string>
#include <unordered_map>
using namespace std;

string greek(const string& input) {
    static const unordered_map<char, char> table = {
        // Φωνήεντα ↔ Ζυγοί αριθμοί
        {'α', '2'}, {'Α', '2'}, {'2', 'α'}, // 2 <-> α/Α
        {'ε', '4'}, {'Ε', '4'}, {'4', 'ε'},
        {'η', '6'}, {'Η', '6'}, {'6', 'η'},
        {'ι', '8'}, {'Ι', '8'}, {'8', 'ι'},
        {'ο', '0'}, {'Ο', '0'}, {'0', 'ο'},
        {'υ', 'ω'}, {'Υ', 'Ω'}, {'ω', 'υ'}, {'Ω', 'Υ'},

        // Μονά ψηφία
        {'1', '9'}, {'9', '1'},
        {'3', '7'}, {'7', '3'},
        {'5', 'ν'}, {'5', 'Ν'}, {'ν', '5'}, {'Ν', '5'},

        // Σύμφωνα (αντιστοιχίες από αρχή–τέλος)
        {'β', 'ψ'}, {'Β', 'Ψ'}, {'ψ', 'β'}, {'Ψ', 'Β'},
        {'γ', 'χ'}, {'Γ', 'Χ'}, {'χ', 'γ'}, {'Χ', 'Γ'},
        {'δ', 'φ'}, {'Δ', 'Φ'}, {'φ', 'δ'}, {'Φ', 'Δ'},
        {'ζ', 'τ'}, {'Ζ', 'Τ'}, {'τ', 'ζ'}, {'Τ', 'Ζ'},
        {'θ', 'σ'}, {'Θ', 'Σ'}, {'σ', 'θ'}, {'Σ', 'Θ'},
        {'κ', 'ρ'}, {'Κ', 'Ρ'}, {'ρ', 'κ'}, {'Ρ', 'Κ'},
        {'λ', 'π'}, {'Λ', 'Π'}, {'π', 'λ'}, {'Π', 'Λ'},
        {'μ', 'ξ'}, {'Μ', 'Ξ'}, {'ξ', 'μ'}, {'Ξ', 'Μ'}
    };

    string output;
    output.reserve(input.size());

    for (char c : input) {
        auto it = table.find(c);
        if (it != table.end()) {
            output += it->second;
        } else {
            output += c; // Αν δεν υπάρχει στον πίνακα, μένει ίδιο
        }
    }

    return output;
}

